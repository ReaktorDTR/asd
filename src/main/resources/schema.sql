DROP TABLE IF EXISTS COUNTRY CASCADE;
CREATE TABLE COUNTRY (
  ID           SERIAL,
  COUNTRY_NAME VARCHAR(50) NOT NULL,
  LANGUAGE     VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID)
);

DROP TABLE IF EXISTS LOCATION CASCADE;
CREATE TABLE LOCATION (
  ID            SERIAL,
  LOCATION_NAME VARCHAR(50) NOT NULL,
  COUNTRY_ID    INT REFERENCES COUNTRY (ID) ON DELETE CASCADE,
  LATITUDE      DECIMAL,
  LONGITUDE     DECIMAL,
  PRIMARY KEY (ID)
);

DROP TABLE IF EXISTS USER_GROUP CASCADE;
CREATE TABLE USER_GROUP (
  ID         SERIAL,
  GROUP_NAME VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID)
);

DROP TABLE IF EXISTS USERS CASCADE;
CREATE TABLE USERS (
  ID            SERIAL,
  USER_NAME     VARCHAR(150) NOT NULL,
  LOCATION_ID   INT REFERENCES LOCATION (ID),
  USER_GROUP_ID INT REFERENCES USER_GROUP (ID) ON DELETE CASCADE,
  PRIMARY KEY (ID)
);

DROP TABLE IF EXISTS SESSION CASCADE;
CREATE TABLE SESSION (
  ID          SERIAL,
  USER_ID     INT REFERENCES USERS (ID) ON DELETE CASCADE,
  DATE_OPENED TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP(2),
  DATE_CLOSED TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP(2),
  PRIMARY KEY (ID)
);

DROP TABLE IF EXISTS REQUEST CASCADE;
CREATE TABLE REQUEST (
  ID         SERIAL,
  URL        VARCHAR(100) NOT NULL,
  METHOD     VARCHAR(10)  NOT NULL,
  PARAMS     VARCHAR(1000),
  SESSION_ID INT REFERENCES SESSION (ID) ON DELETE CASCADE,
  PRIMARY KEY (ID)
);

